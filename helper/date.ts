export function getDay(value: number) {
  switch (value) {
    case 1:
      return 'Sunday'
    case 2:
      return 'Monday'
    case 3:
      return 'TuesDay'
    case 4:
      return 'WednesDay'
    case 5:
      return 'ThursDay'
    case 6:
      return 'Friday'
    case 7:
      return 'Saturday'
  }
}
